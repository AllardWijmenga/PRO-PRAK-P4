        De meeste satelliet-internetservices komen van enkelvoudige geostationaire satellieten in een baan om de planeet op 35.786 km hoogte. Als gevolg daarvan is de tijd voor dataoverdracht tussen de gebruiker en de satelliet - ook bekend als latency - hoog, waardoor het bijna onmogelijk is om streaming, online gaming, videogesprekken of andere activiteiten met hoge datasnelheid te ondersteunen.

        Starlink is een constellatie van duizenden satellieten die veel dichter om de aarde heen draaien, op ongeveer 550 km afstand, en de hele wereld bestrijken. Omdat Starlink-satellieten zich in een lage baan bevinden is de latency aanzienlijk lager; ongeveer 25 ms versus 600 ms of meer.


