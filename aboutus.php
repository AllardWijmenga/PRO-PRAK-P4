        Starlink is de eerste commercieel verkrijgbare phased array-antenne. Uw Starlink-zakelijk-pakket wordt geleverd met alles wat u nodig hebt om in minuten online te gaan, inclusief de Starlink, wifi-router, kabels en het voetstuk. De antenne is zelf-oriënterend en maakt binnen enkele minuten verbinding, zolang hij vrij zicht op de hemel heeft.

